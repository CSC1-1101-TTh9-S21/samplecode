
## Demonstrating how to do for in range loops with while


# one arguments
for i in range(10):
    print(i, end=" ")
print()

i = 0
while i < 10:
    print(i, end=" ")
    i += 1
print()



# two arguments
for i in range(3, 10):
    print(i, end=" ")
print()

i = 3
while i < 10:
    print(i, end=" ")
    i += 1
print()




# three arguments
for i in range(3,10,2):
    print(i, end=" ")
print()

i = 3
while i < 10:
    print(i, end=" ")
    i += 2
print()


